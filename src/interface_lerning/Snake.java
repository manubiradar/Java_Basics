package interface_lerning;

public class Snake implements Animal {

	public void talk() {
		System.out.println("I am a Snake, I belong to "+Animal.type2+" family");
	}

	public void see() {
		System.out.println("I can see only in black and white");
	}

	public void move() {
		System.out.println("I move by crawling");
	}

}
